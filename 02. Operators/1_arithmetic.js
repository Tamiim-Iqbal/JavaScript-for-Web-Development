/* Arithmetic Operator: 
+, -, *, /, modulus(%), exponential(**)
*/

let a = 9;
let b = 2;

console.log("a + b :", a + b);                              //output: 11                   
console.log("a - b :", a - b);                              //output: 7
console.log("a * b :", a * b);                              //output: 18
console.log("a / b :", a / b);                              //output: 4.5
console.log("a % b :", a % b);                              //output: 2
console.log("a ** b :", a ** b);                            //output: 81


// Unary Operator: increment(++), decrement(--)
a++;                    // a = a + 1
console.log(a);                                             //output: 10
b--;                    // b = b - 1
console.log(b);                                             //output: 3

// pre increment/decrement: ++a, --b
// post increment/decrement: a++, b--